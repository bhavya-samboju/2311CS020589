# Declaring variables of different data types
integer_var = 10
float_var = 20.5
string_var = "Hello, World!"
boolean_var = True

# Printing each variable and its type
print("Integer variable:", integer_var)
print("Type of integer variable:", type(integer_var))

print("\nFloat variable:", float_var)
print("Type of float variable:", type(float_var))

print("\nString variable:", string_var)
print("Type of string variable:", type(string_var))

print("\nBoolean variable:", boolean_var)
print("Type of boolean variable:", type(boolean_var))
